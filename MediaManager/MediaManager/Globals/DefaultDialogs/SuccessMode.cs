namespace MediaManager.Globals.DefaultDialogs
{
    /// <summary>
    /// Whether an action was successful
    /// </summary>
    public enum SuccessMode
    {
        /// <summary>
        /// An action was not successful
        /// </summary>
        Error = -1,
        /// <summary>
        /// No success information needed
        /// </summary>
        Neutral = 0,
        /// <summary>
        /// An action was successful
        /// </summary>
        Success = 1
    }
}
